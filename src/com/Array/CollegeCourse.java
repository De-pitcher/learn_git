package com.Array;

public class CollegeCourse {
    String courseID;
    int hrs;
    char letGrade;

    public void setCourseID(String courseID) {
        this.courseID = courseID;
    }

    public String getCourseID() {
        return courseID;
    }

    public void setHrs(int hrs) {
        this.hrs = hrs;
    }

    public int getHrs() {
        return hrs;
    }

    public void setLetGrade(char letGrade) {
        this.letGrade = letGrade;
    }

    public char getLetGrade() {
        return letGrade;
    }
}
